// UPDATE Master_Unit.json OTOMATIS
const fs = require('fs');
const path = 'Master_Unit.json';

// Buat object baru dari list master
const newData = {};
master.split("\n").forEach(line => {
  const code = line.trim();
  if (!code) return;

  const prefix = code.split("-")[0];
  if (!newData[prefix]) newData[prefix] = [];
  newData[prefix].push(code);
});

// Sort tiap prefix berdasarkan bagian kedua (DT/DZ/EX/…) lalu angka terakhir
Object.keys(newData).forEach(prefix => {
  newData[prefix].sort((a, b) => {
    const [p1, mid1, mid2, num1, last1] = a.split("-");
    const [p2, mid3, mid4, num2, last2] = b.split("-");

    // Bandingkan bagian kedua alphabetically
    if (mid1 !== mid3) return mid1.localeCompare(mid3);
    // Bandingkan angka terakhir secara numerik
    return parseInt(last1) - parseInt(last2);
  });
});

// Simpan ke Master_Unit.json (replace total)
fs.writeFileSync(path, JSON.stringify(newData, null, 2));

